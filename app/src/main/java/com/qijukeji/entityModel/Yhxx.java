package com.qijukeji.entityModel;

import java.io.Serializable;

public class Yhxx implements Serializable {
    private static final long serialVersionUID = 1L;
    private int id;
    private String ddlsh;//订单流水号
    private String orderNo;//订单号
    private int memberId;//顾客Id
    private String couponId;//优惠券Id
    private String couponType;//优惠卷类型
    private String couponstyle;//4种优惠类型
    private String man;//满多少
    private String discount;//优惠额度或折扣
    private String money;//优惠券金额
    private String couponExplain;//备注

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDdlsh() {
        return ddlsh;
    }

    public void setDdlsh(String ddlsh) {
        this.ddlsh = ddlsh;
    }

    public String getOrderNo() {
        return orderNo;
    }

    public void setOrderNo(String orderNo) {
        this.orderNo = orderNo;
    }

    public int getMemberId() {
        return memberId;
    }

    public void setMemberId(int memberId) {
        this.memberId = memberId;
    }

    public String getCouponId() {
        return couponId;
    }

    public void setCouponId(String couponId) {
        this.couponId = couponId;
    }

    public String getCouponType() {
        return couponType;
    }

    public void setCouponType(String couponType) {
        this.couponType = couponType;
    }

    public String getCouponstyle() {
        return couponstyle;
    }

    public void setCouponstyle(String couponstyle) {
        couponstyle = couponstyle;
    }

    public String getMan() {
        return man;
    }

    public void setMan(String man) {
        man = man;
    }

    public String getDiscount() {
        return discount;
    }

    public void setDiscount(String discount) {
        this.discount = discount;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getCouponExplain() {
        return couponExplain;
    }

    public void setCouponExplain(String couponExplain) {
        this.couponExplain = couponExplain;
    }

    @Override
    public String toString() {
        return "Yhxx{" +
                "id=" + id +
                ", ddlsh='" + ddlsh + '\'' +
                ", orderNo='" + orderNo + '\'' +
                ", memberId=" + memberId +
                ", couponId='" + couponId + '\'' +
                ", couponType='" + couponType + '\'' +
                ", couponstyle='" + couponstyle + '\'' +
                ", man='" + man + '\'' +
                ", discount='" + discount + '\'' +
                ", money='" + money + '\'' +
                ", couponExplain='" + couponExplain + '\'' +
                '}';
    }
}
